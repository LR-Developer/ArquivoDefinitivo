ALTER PROCEDURE PR_VALIDA_LOGIN
(
	@ID_LOGIN VARCHAR (20),
	@ID_SENHA VARCHAR (6)
)

AS
BEGIN

DECLARE
	@RETURN BIT

IF EXISTS (SELECT ID_LOGIN,ID_SENHA FROM TB_LOGIN WHERE ID_LOGIN = @ID_LOGIN AND ID_SENHA = @ID_SENHA)
	BEGIN
		SET @RETURN = 1
	END
ELSE
	BEGIN
		SET @RETURN = 0
	END

	SELECT @RETURN

END